package operatorsInJava;

public class AssignmentOperator {
    public static void main(String[] args) {
//        +,+=,*=,-=,/=
        int a=10;
        System.out.println(a);
        System.out.println(a+=20);//a=a+20
        System.out.println(a-=20);//10
        System.out.println(a*=20);//200
        System.out.println(a/=20);//10


    }
}
