package operatorsInJava;

public class RelationalOperator {
    public static void main(String[] args) {
        //==,>,<,>=,<=,!=
        int x=200;
        int y=145;
        System.out.println(x==y);//false
        System.out.println(x<y); //false
        System.out.println(x>y);//true
        System.out.println(x<=y);//false
        System.out.println(x>=y);//true
        System.out.println(x!=y);//true

    }
}
