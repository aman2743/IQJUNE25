package statementsInJava.SelectionStatement;

public class Demo1 {
    public static void main(String[] args) {
        int x=10;
        if(x==12) {
            System.out.println("The value of x is 12");
        }
        else if(x==15) {
            System.out.println("The value of x is 15");
        }
        else {
            System.out.println("The value of x is not 12 or 15");
        }
    }
}
